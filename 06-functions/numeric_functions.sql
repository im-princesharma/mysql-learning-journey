-- File: numeric_functions.sql
-- Topic: Numeric Functions
-- Purpose: Perform mathematical operations

SELECT ABS(-50) AS AbsoluteValue;

SELECT ROUND(45.678, 2) AS RoundedValue;

SELECT CEIL(45.2) AS CeilingValue;

SELECT FLOOR(45.9) AS FloorValue;

SELECT MOD(17, 5) AS Remainder;

SELECT POWER(2, 5) AS PowerValue;

SELECT SQRT(64) AS SquareRoot;

SELECT RAND() AS RandomNumber;
